PROGRAM MAIN

IMPLICIT NONE

INTEGER :: N1, N2, I

CHARACTER*32 :: CLARG

CALL GETARG (1, CLARG); READ (CLARG, *) N1
CALL GETARG (2, CLARG); READ (CLARG, *) N2

PRINT *, " N1, N2 = ", N1, N2

!$OMP PARALLEL DO SCHEDULE(STATIC,1) PRIVATE (I)
DO I = N1, N2
  PRINT *, I
ENDDO
!$OMP END PARALLEL DO

END


