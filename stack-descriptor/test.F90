PROGRAM MAIN

IMPLICIT NONE

REAL, POINTER :: X (:)

ALLOCATE (X (1000))

CALL TOTO (X)

CALL TITI (100)

CONTAINS

SUBROUTINE TOTO (P)

REAL, POINTER :: P (:)
REAL, POINTER :: Y (:)

Y => P

!$OMP TARGET ENTER DATA MAP(TO:Y)

END SUBROUTINE

SUBROUTINE TITI (N)

INTEGER :: N

REAL :: Y (N)

!$OMP TARGET DATA MAP (TOFROM: Y)

PRINT *, LOC (Y (1))

!$OMP END TARGET DATA

END SUBROUTINE

END 

